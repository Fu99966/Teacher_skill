from __future__ import annotations


def select_few_shot_examples(subject: str, grade: str, class_type: str, creative_mode: str = "") -> str:
    text = f"{subject} {grade} {class_type} {creative_mode}"
    if "实验" in text or "科学" in text or "物理" in text or "化学" in text:
        return """样例：科学/物理探究课
课堂主线：从反常现象提出问题，学生用变量控制或证据表格解释原因。
关键活动：学生先预测，再观察/实验记录，最后用数据修正原判断。
评价方式：出口卡写“我的证据是……所以我认为……”。"""
    if "复习" in text or "讲评" in text:
        return """样例：中学复习/讲评课
课堂主线：错因分类 -> 方法归纳 -> 变式迁移。
关键活动：学生把典型错误贴到错因墙，归类为概念不清、条件遗漏、方法选择不当。
评价方式：用一道新情境题检验是否真正迁移。"""
    if "公开课" in text or "语文" in text:
        return """样例：小学语文公开课
课堂主线：情境感知 -> 关键词句品读 -> 方法迁移表达。
关键活动：学生抓住一个关键词句，用“我看到/听到/感受到……”表达阅读证据。
评价方式：同伴用“是否有文本证据、表达是否具体”两项互评。"""
    if "大学" in text or "本科" in text or "专科" in text:
        return """样例：大学研讨课
课堂主线：案例进入 -> 理论解释 -> 小组方案 -> 质询答辩。
关键活动：学生阅读短案例，提出方案假设，并接受同伴质询。
评价方式：从问题界定、证据引用、方案可行性三维评价。"""
    return """样例：通用高质量教案
课堂主线：真实问题驱动，学生先形成初始观点，再通过材料、活动或例题修正观点。
关键活动：至少有一次可观察的学生产出，例如证据表、概念图、解题说明或展示稿。
评价方式：课堂末用出口卡或小测收集达成证据。"""
